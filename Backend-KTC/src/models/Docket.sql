CREATE TABLE IF NOT EXISTS Docket(  
    DocketNo VARCHAR(255) NOT NULL PRIMARY KEY,
    Branch VARCHAR(255) NOT NULL,
    Date DATE NOT NULL,
    Source VARCHAR(255) NOT NULL,
    Destination VARCHAR(255) NOT NULL,
    TruckNo VARCHAR(255) NOT NULL,
    Size VARCHAR(255) NOT NULL,
    ActualWeight VARCHAR(255) NOT NULL,
    TruckFright VARCHAR(255) NOT NULL,
    ConsignorName VARCHAR(255) NOT NULL,
    ConsignorAddress VARCHAR(255) NOT NULL,
    ConsignorGST VARCHAR(255) NOT NULL,
    ConsigneeName VARCHAR(255) NOT NULL,
    ConsigneeAddress VARCHAR(255) NOT NULL,
    ConsigneeGST VARCHAR(255) NOT NULL,
    Product VARCHAR(255) NOT NULL,
    TotalPackages VARCHAR(255) NOT NULL,
    MethodOfPkg VARCHAR(255) NOT NULL,
    InvoiceNo VARCHAR(255),
    DeclaredValue VARCHAR(255) DEFAULT 'As Per Bill',
    EwayBillNo VARCHAR(255) NOT NULL,
    ProvisionalAmount VARCHAR(255) NOT NULL,
    GreenTax VARCHAR(255) NOT NULL DEFAULT '0',
    LabourCharges VARCHAR(255) NOT NULL DEFAULT '0',
    HoldingCharges VARCHAR(255) NOT NULL DEFAULT '0',
    MultiPointPickUpCharges VARCHAR(255) NOT NULL DEFAULT '0',
    MultiPointDileveryCharges VARCHAR(255) NOT NULL DEFAULT '0',
    DocketCharges VARCHAR(255) NOT NULL DEFAULT '0',
    OtherCharges VARCHAR(255) NOT NULL DEFAULT '0',
    PaymentMode VARCHAR(255) NOT NULL DEFAULT '0',
    TotalAmount VARCHAR(255) NOT NULL,
    GSTINPayableBy VARCHAR(255) NOT NULL,
    BillingBranch VARCHAR(255) NOT NULL,
    Remarks VARCHAR(255),
    Timestamp TIMESTAMP DEFAULT CURRENT_TIMESTAMP
) COMMENT 'Docket_Data';